   private void cargarCombo(){
       JCmateria.removeAllItems();
        for (Materia materia : mat ) {
            JCmateria.addItem(materia);
        }

        JCalumno.removeAllItems();
        for (Alumno alumno : alum ) {
            JCalumno.addItem(alumno);
        }
   }

